-- Pastikan Rayfield sudah di-load

local Rayfield = loadstring(game:HttpGet('https://sirius.menu/rayfield'))()

local ReplicatedStorage = game:GetService("ReplicatedStorage")
local player = game.Players.LocalPlayer

-- üîß Lokasi Remote (mengikuti struktur sleitnick_net@0.2.0)
local net = ReplicatedStorage
	:WaitForChild("Packages")
	:WaitForChild("_Index")
	:WaitForChild("sleitnick_net@0.2.0")
	:WaitForChild("net")

-- Pastikan folder RF dan RE ada
local equipRemote = net:WaitForChild("RE/EquipToolFromHotbar")
local rodRemote = net:WaitForChild("RF/ChargeFishingRod")
local miniGameRemote = net:WaitForChild("RF/RequestFishingMinigameStarted")
local finishRemote = net:WaitForChild("RE/FishingCompleted")

-- Remote penting
local equipRemote = RE:WaitForChild("EquipToolFromHotbar")
local rodRemote = RF:WaitForChild("ChargeFishingRod")
local miniGameRemote = RF:WaitForChild("RequestFishingMinigameStarted")
local finishRemote = RE:WaitForChild("FishingCompleted")

-- Variabel utama
local autofish = false
local perfectCast = false
local autoRecastDelay = 2
local fishCount = 0

-- ‚öôÔ∏è GUI Setup
local Window = Rayfield:CreateWindow({
	Name = "üé£ Auto Fishing Hub",
	LoadingTitle = "Fishing AutoFarm",
	LoadingSubtitle = "By HyRexxyy x GPT",
	ConfigurationSaving = {
		Enabled = true,
		FolderName = "AutoFishSettings"
	},
	KeySystem = false
})

local MainTab = Window:CreateTab("‚öôÔ∏è Main Controls")
local CounterLabel = MainTab:CreateLabel("üêü Fish Caught: 0")

-- üé£ AUTO FISH TOGGLE
MainTab:CreateToggle({
	Name = "üé£ Enable Auto Fishing",
	CurrentValue = false,
	Callback = function(val)
		autofish = val

		if val then
			task.spawn(function()
				while autofish do
					pcall(function()
						-- Equip alat pancing
						equipRemote:FireServer(1)
						task.wait(0.1)

						-- Casting
						local timestamp = perfectCast and 9999999999 or (tick() + math.random())
						rodRemote:InvokeServer(timestamp)
						task.wait(0.2)

						-- Mini game
						local x = perfectCast and -1.238 or (math.random(-1000, 1000) / 1000)
						local y = perfectCast and 0.969 or (math.random(0, 1000) / 1000)
						miniGameRemote:InvokeServer(x, y)

						-- Reel & selesai
						task.wait(1.2)
						finishRemote:FireServer()

						-- Counter naik
						fishCount += 1
						CounterLabel:SetText("üêü Fish Caught: " .. fishCount)
					end)
					task.wait(autoRecastDelay)
				end
			end)
		end
	end
})

-- ‚ú® PERFECT CAST TOGGLE
MainTab:CreateToggle({
	Name = "‚ú® Use Perfect Cast",
	CurrentValue = false,
	Callback = function(val)
		perfectCast = val
	end
})

-- ‚è±Ô∏è RECAST DELAY SLIDER
MainTab:CreateSlider({
	Name = "‚è±Ô∏è Auto Recast Delay (seconds)",
	Range = {0.5, 5},
	Increment = 0.1,
	CurrentValue = autoRecastDelay,
	Callback = function(val)
		autoRecastDelay = val
	end
})

-- ‚ùå CLOSE GUI BUTTON
MainTab:CreateButton({
	Name = "‚ùå Close GUI",
	Callback = function()
		autofish = false
		Rayfield:Destroy()
	end
})

-- ‚úÖ Notifikasi awal
Rayfield:Notify({
	Title = "‚úÖ AutoFish GUI Loaded",
	Content = "All remotes connected successfully!",
	Duration = 4
})
